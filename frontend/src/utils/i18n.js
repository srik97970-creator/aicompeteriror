// Simple reactive state hook for UI translations
export function useTranslation() {
    const [lang, setLangState] = useState(() => {
        const saved = localStorage.getItem('ui_lang');
        if (saved === 'en' || saved === 'hi' || saved === 'te') {
            return saved;
        }
        return 'en';
    });
    const setLanguage = (newLang) => {
        localStorage.setItem('ui_lang', newLang);
        setLangState(newLang);
        window.dispatchEvent(new Event('languagechange'));
    };
    useEffect(() => {
        const handleLangChange = () => {
            const current = localStorage.getItem('ui_lang');
            if (current === 'en' || current === 'hi' || current === 'te') {
                setLangState(current);
            }
            else {
                setLangState('en');
            }
        };
        window.addEventListener('languagechange', handleLangChange);
        return () => {
            window.removeEventListener('languagechange', handleLangChange);
        };
    }, []);
    const t = (key, interpolations) => {
        const dict = translations[lang] || translations['en'];
        let text = dict[key] || translations['en'][key] || String(key);
        if (interpolations) {
            Object.entries(interpolations).forEach(([k, v]) => {
                text = text.replace(`{${k}}`, v);
            });
        }
        return text;
    };
    return { t, currentLanguage: lang, setLanguage };
}
